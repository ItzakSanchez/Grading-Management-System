DROP DATABASE IF EXISTS grade_manager_system;
CREATE DATABASE grade_manager_system;

USE grade_manager_system;

CREATE TABLE TBL_SYSTEM_USER(
ID int AUTO_INCREMENT NOT NULL,
FIRST_NAME varchar(255),
LAST_NAME varchar(255),
EMAIL varchar(255),
PASSWORD varchar(255),
ROLE varchar(255),
PRIMARY KEY (ID)
);

CREATE TABLE TBL_STUDENT(
ID int AUTO_INCREMENT NOT NULL,
ID_SYSTEM_USER int,
PRIMARY KEY (ID)
);

CREATE TABLE TBL_TEACHER(
ID int AUTO_INCREMENT NOT NULL,
ID_SYSTEM_USER int,
PRIMARY KEY (ID)
);

CREATE TABLE TBL_COURSE(
ID int NOT NULL AUTO_INCREMENT,
COURSE_NAME varchar(255),
COURSE_DESCRIPTION varchar(255),
PRIMARY KEY (ID)
);

CREATE TABLE TBL_GROUP(
ID int NOT NULL AUTO_INCREMENT,
ID_COURSE INT,
ID_TEACHER INT,
GROUP_SEMESTER varchar(255),
GROUP_YEAR varchar(255),
PRIMARY KEY (ID)
);

CREATE TABLE TBL_INSCRIPTIONS(
ID int NOT NULL AUTO_INCREMENT,
ID_GROUP varchar(255),
ID_STUDENT varchar(255),
SCORE decimal(5,2),
PRIMARY KEY (ID)
);